import UIKit

extension UIColor {
    
    class func swiftLightOrange() -> UIColor {
        return UIColor(red: 255 / 255, green: 133 / 255, blue: 43 / 255, alpha: 1)
    }
    
    class func swiftMediumOrange() -> UIColor {
        return UIColor(red: 249 / 255, green: 77 / 255, blue: 30 / 255, alpha: 1)
    }
    
    class func swiftDarkOrange() -> UIColor {
        return UIColor(red: 251 / 255, green: 72 / 255, blue: 31 / 255, alpha: 1)
    }
    
    class func swiftGreen() -> UIColor {
        return UIColor(red: 78 / 255, green: 194 / 255, blue: 78 / 255, alpha: 1)
    }
    
    class func drawerColor() -> UIColor {
        return UIColor(red: 0.32, green: 0.26, blue: 0.25, alpha: 1)
    }
    
    class func swiftLightGray() -> UIColor {
        return UIColor(red: 235 / 255, green: 235 / 255, blue: 235 / 255, alpha: 1)
    }
    
    class func swiftMediumGray() -> UIColor {
        return UIColor(red: 160 / 255, green: 160 / 255, blue: 160 / 255, alpha: 1)
    }
    
}
